    /** 全プリセットデッキ。 */
    static List<Deck> all() {
        return List.of(volcanoAggro(), seaControl(), forestMid(), mixed());
    }

    private static Deck deck(String name, Object... pairs) {
        Deck d = new Deck(name);
        for (int i = 0; i + 1 < pairs.length; i += 2) {
            d.set((String) pairs[i], (Integer) pairs[i + 1]);
        }
        return d;
    }

